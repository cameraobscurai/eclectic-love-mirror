#!/usr/bin/env node
// Read /tmp/gallery-audit.json and emit a SQL migration that drops
// perceptual-duplicate images at distance ≤ MAX_DISTANCE from inventory_items.images.
// Keeps the first occurrence of each cluster.
//
// Usage:
//   node scripts/apply-gallery-audit.mjs            # dry-run, prints summary + SQL to stdout
//   node scripts/apply-gallery-audit.mjs --max=3    # set distance threshold (default 3)

import { execSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";

const MAX_DIST = Number((process.argv.find((a) => a.startsWith("--max="))?.split("=")[1]) ?? 3);
const audit = JSON.parse(readFileSync("/tmp/gallery-audit.json", "utf8"));

const updates = [];
let totalDropped = 0;

for (const f of audit.findings) {
  const dupes = f.dupes.filter((d) => d.distance <= MAX_DIST);
  if (!dupes.length) continue;

  // union-find clusters across pairs
  const parent = new Map();
  const find = (x) => {
    if (parent.get(x) !== x) parent.set(x, find(parent.get(x)));
    return parent.get(x);
  };
  const union = (a, b) => {
    const ra = find(a), rb = find(b);
    if (ra !== rb) parent.set(Math.max(ra, rb), Math.min(ra, rb));
  };
  for (let i = 0; i < f.imageCount; i++) parent.set(i, i);
  for (const d of dupes) union(d.i, d.j);

  // keep min index per cluster
  const keep = new Set();
  const seenRoots = new Set();
  for (let i = 0; i < f.imageCount; i++) {
    const r = find(i);
    if (!seenRoots.has(r)) {
      seenRoots.add(r);
      keep.add(i);
    }
  }

  if (keep.size === f.imageCount) continue;
  const dropped = f.imageCount - keep.size;
  totalDropped += dropped;
  updates.push({ rms_id: f.rms_id, slug: f.slug, kept: [...keep].sort((a, b) => a - b), dropped, imageCount: f.imageCount });
}

console.error(`max distance: ${MAX_DIST}`);
console.error(`items to update: ${updates.length}`);
console.error(`images to drop: ${totalDropped}`);

// build SQL — fetch current images per rms_id and emit a static array literal
const rmsIds = updates.map((u) => `'${u.rms_id}'`).join(",");
const rows = execSync(
  `psql -At -F$'\\t' -c "SELECT rms_id, array_to_string(images, '|||') FROM inventory_items WHERE rms_id IN (${rmsIds})"`,
  { encoding: "utf8", maxBuffer: 32 * 1024 * 1024 }
)
  .trim()
  .split("\n")
  .map((line) => {
    const [rms_id, joined] = line.split("\t");
    return [rms_id, (joined || "").split("|||").filter(Boolean)];
  });
const imagesByRms = new Map(rows);

const sqlEscape = (s) => s.replace(/'/g, "''");
const sql = [];
sql.push(`-- Auto-generated by scripts/apply-gallery-audit.mjs (max distance ${MAX_DIST})`);
sql.push(`-- Drops ${totalDropped} perceptual-duplicate images across ${updates.length} items.`);
sql.push("");
for (const u of updates) {
  const current = imagesByRms.get(u.rms_id);
  if (!current || current.length !== u.imageCount) {
    console.error(`SKIP ${u.rms_id} ${u.slug}: image count drift (db=${current?.length} audit=${u.imageCount})`);
    continue;
  }
  const next = u.kept.map((i) => current[i]);
  const arr = "ARRAY[" + next.map((s) => `'${sqlEscape(s)}'`).join(", ") + "]::text[]";
  sql.push(`-- ${u.slug}: ${u.imageCount} → ${next.length}`);
  sql.push(`UPDATE public.inventory_items SET images = ${arr} WHERE rms_id = '${u.rms_id}';`);
}

writeFileSync("/tmp/gallery-dedup.sql", sql.join("\n") + "\n");
console.error(`→ /tmp/gallery-dedup.sql`);
